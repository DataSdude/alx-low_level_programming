/**
 *  * _abs - function that compute the absolute
 *
 *   *       value of an integer
 *
 *    * @n: takes in integer type input for function
 *
 *     *
 *
 *      * Return: (n) if is positive
 *		(-n) if is negative
 */

int _abs(int n)
{
	if (n >= 0)
		return (n);

	return (-n);
}
